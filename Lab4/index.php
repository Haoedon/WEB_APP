    <?php
    // ========================================================================
    // ИНИЦИАЛИЗАЦИЯ ПЕРЕМЕННЫХ И ПРОВЕРКА УСЛОВИЙ
    // ========================================================================

    // В начале программы необходимо инициализировать переменную, 
    // которая бы содержала число колонок таблиц.
    $columnsCount = 5; 

    // Если задано нулевое число колонок – таблицы не выводятся, 
    // выводится надпись: "Неправильное число колонок".
    if ($columnsCount <= 0) {
        echo '<div class="error-message">Неправильное число колонок</div>';
        // Прерываем выполнение, так как таблицы выводить нельзя
        $structures = array(); 
    } else {
        // В начале программы задается массив с не менее 10 разными элементами, 
        // содержащие строки формата: "C1*C2*C3#C4*C5*C6# ... Cn"
        $structures = array(
            'Ячейка1*Ячейка2*Ячейка3#Ячейка4*Ячейка5',
            'Данные1*Данные2#Данные3*Данные4*Данные5',
            'Тест1*Тест2*Тест3*Тест4*Тест5#Новая строка',
            'А*Б*В#Г*Д#Е*Ж*З',
            'Один*Два#Три*Четыре*Пять',
            'Строка1#Строка2#Строка3',
            'Кол1*Кол2*Кол3*Кол4*Кол5',
            'Пустая*Ячейка*#*Данные', // Пример с пустой ячейкой внутри
            '#Начало с разделителя*Данные',
            'Конечные*Данные*Без*Разделителя#Строки'
        );
    }

    // ========================================================================
    // ФУНКЦИИ (Согласно Листингам А-4.1 и А-4.3 с доработками)
    // ========================================================================

    /**
     * Функция формирования содержимого отдельной строки таблицы.
     * Основана на Листинге А-4.1.
     * 
     * @param string $data Структура строки (разделитель *)
     * @param int $colCount Требуемое количество колонок
     * @return string HTML код строки таблицы или пустая строка
     */
    function getTR($data, $colCount) {
        // Разбиваем строку в массив (разделитель *)
        // Примечание: В листинге использовано '\*', но по условию разделитель '*'
        $arr = explode('*', $data); 
        $ret = '<tr>'; // Начинаем тег строки таблицы
        $hasContent = false; // Флаг наличия содержимого

        // Цикл по всем ячейкам таблицы (согласно Листингу А-4.1)
        // Ограничиваем вывод требуемым числом колонок
        $limit = ($colCount < count($arr)) ? $colCount : count($arr);
        
        for ($i = 0; $i < $limit; $i++) {
            $cellContent = $arr[$i];
            $ret .= '<td>' . $cellContent . '</td>'; // Добавляем ячейкам тег
            if (trim($cellContent) !== '') {
                $hasContent = true;
            }
        }

        // Таблицы всегда должны выводиться с требуемым числом колонок 
        // (возможно с пустыми ячейками в них)
        for ($i = count($arr); $i < $colCount; $i++) {
            $ret .= '<td></td>'; 
        }

        $ret .= '</tr>'; // Закрывающий тег строки

        // Если в строке нет ячеек (пустая строка или только пустые значения) – 
        // ее HTML-код не выводится (возвращаем пустую строку)
        // Примечание: Если данные были переданы, но они пустые, считаем строку пустой
        if (trim($data) === '') {
            return '';
        }

        return $ret; // Возвращаем строку таблицы
    }

    /**
     * Функция вывода HTML-кода таблицы исходя из ее структуры.
     * 
     * 
     * @param string $structure Структура таблицы (разделитель #)
     * @param int $num Номер таблицы
     * @param int $colCount Требуемое количество колонок
     */
    function outTable($structure, $num, $colCount) {
        // Перед выводом каждой новой таблицы необходимо добавить заголовок второго уровня
        echo '<h2>Таблица №' . $num . '</h2>';

        // Разбиваем структуру на строки (разделитель #)
        $strings = explode('#', $structure); 
        $datas = ''; // Итоговый HTML-код строк

        // Проверка: Если в структуре таблицы нет строк (пустая структура)
        // explode возвращает массив с одним пустым элементом для пустой строки
        if (empty($structure)) {
            echo '<div class="error-message">В таблице нет строк</div>';
            return;
        }

        // Цикл для всех строк из структуры 
        for ($i = 0; $i < count($strings); $i++) {
            // Добавляем код строки в итоговый
            $rowHtml = getTR($strings[$i], $colCount);
            
            // Если функция вернула пустую строку (нет ячеек), не добавляем
            if ($rowHtml !== '') {
                $datas .= $rowHtml; 
            }
        }

        // Вывод таблицы или сообщений об ошибках
        if ($datas) { 
            // Если код строк определен
            echo '<table>' . $datas . '</table>'; // Выводим таблицу
        } else {
            // Иначе
            // Различаем "нет строк с ячейками" и "нет строк"
            // Так как проверка empty($structure) была выше, здесь значит строки были, но пустые
            echo '<div class="error-message">В таблице нет строк с ячейками</div>'; 
        }
    }

    // Массив со структурами таблиц уже определен выше в $structures
    // Для всех элементов массива выводим соответствующую структуре таблицу
    if ($columnsCount > 0 && !empty($structures)) {
        for ($i = 0; $i < count($structures); $i++) { 
            outTable($structures[$i], ($i + 1), $columnsCount); 
        }
    }
    ?>

